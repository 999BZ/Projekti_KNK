CREATE TABLE Teacher (
	T_ID INT NOT NULL AUTO_INCREMENT,
    T_Name VARCHAR(50),
    T_Surname VARCHAR(50),
    T_Birthdate Date,
    T_Phone VARCHAR(50),
    T_Address VARCHAR(50),
    T_UID INT NOT NULL,
    PRIMARY KEY (T_ID),
    FOREIGN KEY (T_UID) REFERENCES Users(U_ID) ON DELETE CASCADE
);
#SELECT * FROM Teacher;

CREATE TABLE Enrollment(
	E_ID INT NOT NULL,
    S_ID INT NOT NULL,
    T_ID INT NOT NULL,
    E_Date Date,
    PRIMARY KEY (E_ID),
    FOREIGN KEY (S_ID) REFERENCES Students(S_ID) ON DELETE CASCADE,
    FOREIGN KEY (T_ID) REFERENCES Teacher(T_ID) ON DELETE CASCADE
);
#SELECT * FROM Enrollment;

CREATE TABLE Courses(
	Course_ID INT NOT NULL,
    T_ID INT NOT NULL,
    C_Name VARCHAR(50),
    S_GLevel INT NOT NULL,
    PRIMARY KEY (Course_ID),
    FOREIGN KEY (T_ID) REFERENCES Teacher(T_ID) ON DELETE CASCADE
);
#SELECT * FROM Courses;

CREATE TABLE Grades(
	G_ID INT NOT NULL,
    S_ID INT NOT NULL,
    A_ID INT NOT NULL,
    G_Value INT,
    PRIMARY KEY (G_ID),
    FOREIGN KEY (S_ID) REFERENCES Students(S_ID) ON DELETE CASCADE
);
